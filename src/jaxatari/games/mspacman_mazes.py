       if len(digits) == 0:    
              return jnp.array(digits)
	
       # Raise an error if there are less than 3 color channels (RGB) for each pixel
       if not np.all(d.shape[2] >= 3 for d in digits):
              raise ValueError("All digit frames must have at least 3 color channels (RGB) per pixel!")

       # Determine target dimensions
       max_w = max([d.shape[0] for d in digits])
       max_h = max([d.shape[1] for d in digits])
	
       # Pad each digit to the target dimensions
       padded_digits = []
       for d in digits:
           pad_w = max_w - d.shape[0]
           pad_h = max_h - d.shape[1]
